import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const frontendRoot = path.resolve(__dirname, "..");
const contractDirs = [
  path.join(frontendRoot, "ui-contracts", "default-shell"),
  path.join(frontendRoot, "ui-contracts", "retro-shell"),
];
const outputPath = path.join(frontendRoot, "app", "generated", "ui-tokens.css");
const checkMode = process.argv.includes("--check");

function collectTokens(node, pathParts = [], bucket = []) {
  if (!node || typeof node !== "object" || Array.isArray(node)) {
    return bucket;
  }

  if (Object.hasOwn(node, "$value")) {
    bucket.push({
      name: pathParts.join("-"),
      value: String(node.$value),
    });
    return bucket;
  }

  for (const [key, value] of Object.entries(node)) {
    collectTokens(value, [...pathParts, key], bucket);
  }
  return bucket;
}

async function loadContractTokens(contractDir) {
  const sourcePath = path.join(contractDir, "baseline.tokens.json");
  const content = await readFile(sourcePath, "utf8");
  const parsed = JSON.parse(content);
  return collectTokens(parsed).map((token) => ({
    ...token,
    source: path.relative(frontendRoot, sourcePath),
  }));
}

function renderCss(tokens) {
  const lines = [
    "/* Auto-generated by scripts/build-ui-tokens.mjs. Do not edit manually. */",
    ":root {",
  ];

  for (const token of tokens) {
    lines.push(`  --${token.name}: ${token.value};`);
  }

  lines.push("}", "");
  return lines.join("\n");
}

async function main() {
  const tokens = (
    await Promise.all(contractDirs.map((contractDir) => loadContractTokens(contractDir)))
  )
    .flat()
    .sort((left, right) => left.name.localeCompare(right.name));

  const rendered = renderCss(tokens);

  if (checkMode) {
    const existing = await readFile(outputPath, "utf8").catch(() => null);
    if (existing !== rendered) {
      console.error(
        `Generated token CSS is out of date: ${path.relative(frontendRoot, outputPath)}`
      );
      console.error("Run `npm run tokens:build` in apps/web/frontend.");
      process.exit(1);
    }
    return;
  }

  await writeFile(outputPath, rendered, "utf8");
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
